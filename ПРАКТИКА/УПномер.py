def max_guests(n, m):
    # n — общее количество этажей в гостинице
    # Номера начинают располагаться со второго этажа,
    # значит на этажах с номерами от 2 до n включительно
    floors_with_rooms = n - 1  # Этажей с номерами

    # m — количество номеров на каждом этаже
    # Каждый номер вмещает 3 гостей
    guests_per_room = 3

    # Общее количество гостей — произведение количества этажей с номерами,
    # количества номеров на этаже и вместимости номера
    total_guests = floors_with_rooms * m * guests_per_room

    return total_guests

print(max_guests(5, 10))
print(max_guests(10, 10))
print(max_guests(2, 5))




























